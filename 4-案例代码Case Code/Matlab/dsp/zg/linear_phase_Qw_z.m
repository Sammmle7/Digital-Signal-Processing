function Qw = linear_phase_Qw_z(h)
% Zhou Zhiguo
% 2019.5.6
% ÏàÆµº¯Êý
%--------------------------------------------------------------------------
if firtype(h) == 1 
%--------------------------------------------------------------------------
    N = length(h);
    K = 512;
    dw = 2*pi/K;
    Qw = zeros(1,K);
    
    for k =0:1:K-1
        Qw(k+1) = -(k*dw*(N-1)/2);
    end
%--------------------------------------------------------------------------
elseif firtype(h) == 2
%--------------------------------------------------------------------------
    N = length(h);
    K = 512;
    dw = 2*pi/K;
    Qw = zeros(1,K);
    
    for k =0:1:K-1
        Qw(k+1) = -(k*dw*(N-1)/2);
    end
%--------------------------------------------------------------------------   
elseif firtype(h) == 3
%--------------------------------------------------------------------------
    N = length(h);
    K = 512;
    dw = 2*pi/K;
    Qw = zeros(1,K);
    
    for k =0:1:K-1
        Qw(k+1) = -(k*dw*(N-1)/2) + 0.5*pi;
    end
%--------------------------------------------------------------------------
elseif firtype(h) == 4
%--------------------------------------------------------------------------
    N = length(h);
    K = 512;
    dw = 2*pi/K;
    Qw = zeros(1,K);
    
    for k =0:1:K-1
        Qw(k+1) = -(k*dw*(N-1)/2) + 0.5*pi;
    end

end %if firtype

end %fun

